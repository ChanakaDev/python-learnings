'''
Value assignment for multiple variables
=======================================
'''
num1 = 10
num2 = 20
num3 = 30
print("\nnum 1: " + str(num1))
print("num 2: " + str(num2))
print("num 3: " + str(num3))


num1, num2, num3 = 100,500,1500
print("\nnum 1: " + str(num1))
print("num 2: " + str(num2))
print("num 3: " + str(num3))

age1 = age2 = age3 = 25
print("\nage 1: " + str(age1))
print("age 2: " + str(age2))
print("age 3: " + str(age3))